#include <iostream>
using namespace std;

int main()
{
    int a = 4, b = 5;
    cout << "This value of a+b=" << a + b << endl;
    cout << "This value of a-b=" << a - b << endl;
    cout << "This value of a/b=" << a / b << endl;
    cout << "This value of a*b=" << a * b << endl;
    cout << "This value of a++=" << a++ << endl;
    cout << "This value of b++=" << b++ << endl;
    cout << "This value of ++a=" << ++a << endl;
    cout << "This value of ++a=" << ++b << endl;
    cout << "This value of a--=" << a-- << endl;
    cout << "This value of b--=" << b-- << endl;
    cout << "This value of b--=" << --a << endl;
    cout << "This value of b--=" << --b << endl;

    // This PROGRAM has All Maths Rulse Apply//
    // than New Trick =,<,> ect..//
    cout << "Follwing are the type of logicel oprators in C++" << endl;
    cout << "This value of a==b" << (a == b) << endl;
    cout << "This value of a!=b" << (a != b) << endl;
    cout << "This value of a<b " << (a < b) << endl;
    cout << "This value of a>b " << (a > b) << endl;
    cout << "This value of a<=b" << (a <= b) << endl;
    cout << "This value of a>=b" << (a >= b) << endl;
    return 0;
}